<script setup>
import { onMounted, ref } from "vue";
import { storeToRefs } from "pinia";
import { useUserDataStore } from "@/stores/user-data";
import { useEyewitnessInfoStore } from "@/stores/eyewitness-info";
import { useStateStore } from "@/stores/state";
import { FontAwesomeIcon } from "@fortawesome/vue-fontawesome";
import axios from "axios";

const userDataStore = useUserDataStore();
const stateStore = useStateStore();
const {
  getCurrentPosition,
  getSheetNameAndExpiredTime,
  getFilteredData,
} = useUserDataStore();
const { currentLatitude, currentLongitude } = storeToRefs(userDataStore);
const { loadingStyle } = storeToRefs(stateStore);
const { eyewitnessInfo, monsterList, getDistance } = useEyewitnessInfoStore();

const rareMonsterList = ["櫻火龍", "黑角龍"];
const googleScriptUrl =
  "https://script.google.com/macros/s/AKfycbzNz-T9LWaL7RZN5e2C06Tfsk27nSTqTqi7ndTJErFI5NOD5dqRtwKB5VxcsoeZmv8-0Q/exec";

const handleSubmit = async () => {
  try {
    if (
      eyewitnessInfo.round > 5 ||
      eyewitnessInfo.round < 1 ||
      eyewitnessInfo.rare > 10 ||
      eyewitnessInfo.rare < 1
    ) {
      alert("周目(1~5)或星數(1~10)超過範圍");
      return;
    } else if (
      confirm(`
        請確認以下情報是否正確？
        目擊地區為： ${eyewitnessInfo.isPark ? "公園地區" : "一般地區"}
        目擊魔物為： ${eyewitnessInfo.monsterName}
        周目為： ${eyewitnessInfo.round}
        星數為： ${eyewitnessInfo.rare}
        `)
    ) {
      const { sheetName, expiredTime } = getSheetNameAndExpiredTime(
        eyewitnessInfo.isPark
      );
      const now = moment();

      loadingStyle.value = true;
      await getCurrentPosition();

      const payload = {
        sheetName,
        time: now.format(),
        expiredTime,
        isPark: eyewitnessInfo.isPark,
        monsterName: eyewitnessInfo.monsterName,
        round: eyewitnessInfo.round,
        rare: eyewitnessInfo.rare,
        latitude: currentLatitude.value,
        longitude: currentLongitude.value,
        mapLink: `https://www.google.com/maps/place/${currentLatitude.value},${currentLongitude.value}`,
      };

      const res = await axios.post(googleScriptUrl, payload, {
        headers: {
          "Content-Type": "application/x-www-form-urlencoded",
        },
      });

      localStorage.setItem("submitedData", JSON.stringify(payload));
      loadingStyle.value = false;
      alert(res.data);
    } else {
      return;
    }
  } catch (error) {
    console.error(error);
    alert(`
    發布失敗，請向開發者回報以下錯誤訊息
    ${error}
    `);
    loadingStyle.value = false;
  }
};

const fetchMonsterList = async () => {
  try {
    const now = encodeURIComponent(moment().format());
    const { sheetName } = getSheetNameAndExpiredTime(eyewitnessInfo.isPark);

    loadingStyle.value = true;

    const res = await axios.get(googleScriptUrl + `?sheetName=${sheetName}&now=${now}`);
    await getCurrentPosition();

    const tempRes = res.data.map((data) => {
      let distance = getDistance(
        currentLatitude.value,
        currentLongitude.value,
        data[7],
        data[8],
        "K"
      );

      const monsterInfo = {
        serialNum: data[0],
        submitedTime: data[1],
        expiredTime: data[2],
        isPark: data[3],
        monsterName: data[4],
        round: data[5],
        rare: data[6],
        lat: data[7],
        lng: data[8],
        mapLink: data[9],
        expiredTime: data[10],
        distance: Number.parseFloat(distance).toFixed(3),
      };

      return monsterInfo;
    });

    monsterList.value = getFilteredData(tempRes).sort((a, b) => a.distance - b.distance);

    if (!monsterList.value.length) alert("沒有符合篩選條件的情報");

    loadingStyle.value = false;
  } catch (error) {
    console.error(error);
    monsterList.value = [];
    alert("暫無魔物目擊情報");
    loadingStyle.value = false;
  }
};

onMounted(async () => {
  const submitedData = JSON.parse(localStorage.getItem("submitedData"));
  if (submitedData) {
    const { monsterName, round, rare } = submitedData;
    eyewitnessInfo.monsterName = monsterName;
    eyewitnessInfo.round = round;
    eyewitnessInfo.rare = rare;
  }
});
</script>

<template>
  <div class="container">
    <button class="map">
      <font-awesome-icon icon="fa-solid fa-map" size="xl" />
    </button>
    <button class="monster">
      <font-awesome-icon icon="fa-solid fa-filter" size="xl" />
    </button>
    <button
      type="button"
      data-bs-toggle="modal"
      data-bs-target="#announceModal"
      class="announce"
    >
      <font-awesome-icon icon="fa-solid fa-arrow-up-from-bracket" size="xl" />
    </button>
    <button class="search" @click="fetchMonsterList">
      <font-awesome-icon icon="fa-solid fa-magnifying-glass" size="xl" />
    </button>
  </div>

  <div
    class="modal fade"
    id="announceModal"
    tabindex="-1"
    aria-labelledby="announceModalLabel"
    aria-hidden="true"
  >
    <div class="modal-dialog modal-dialog modal-dialog-centered">
      <div class="modal-content">
        <div class="modal-header">
          <h1 class="modal-title fs-5" id="announceModalLabel">發布目擊情報</h1>
        </div>
        <div class="modal-body">
          <div
            class="round-and-park d-flex justify-content-center align-items-center mb-3"
          >
            <div class="round mx-2">
              <font-awesome-icon
                v-for="n in 5"
                :icon="`fa-solid fa-${n}`"
                size="xl"
                class="round-check"
                :class="{ checked: eyewitnessInfo.round === n }"
                @click="
                  () => {
                    eyewitnessInfo.round = n;
                    eyewitnessInfo.rare = n + 4;
                  }
                "
              />
            </div>

            <div class="park px-3">
              <font-awesome-icon
                icon="fa-solid fa-tree"
                size="2xl"
                class="park-icon"
                :class="{ 'park-check': eyewitnessInfo.isPark }"
                @click="() => (eyewitnessInfo.isPark = !eyewitnessInfo.isPark)"
              />
            </div>
          </div>

          <div class="rare d-flex justify-content-center">
            <font-awesome-icon
              icon="fa-solid fa-star"
              size="2xl"
              class="rare-check"
              style="color: orange"
              @click="() => (eyewitnessInfo.rare = 5)"
            />

            <font-awesome-icon
              v-for="n in eyewitnessInfo.round - 1"
              icon="fa-solid fa-star"
              size="2xl"
              class="rare-check"
              :class="{ 'purple-star': eyewitnessInfo.rare >= n + 5 }"
              @click="() => (eyewitnessInfo.rare = n + 5)"
            />
          </div>

          <div class="monster-name d-flex justify-content-evenly">
            <img
              v-for="monsterName in rareMonsterList"
              class="monster-img"
              :class="{ 'name-check': eyewitnessInfo.monsterName === monsterName }"
              :src="'/images/' + monsterName + '.png'"
              :alt="monsterName + '的圖片'"
              @click="
                () => {
                  eyewitnessInfo.monsterName = monsterName;
                }
              "
            />
          </div>
        </div>

        <div class="modal-footer">
          <button type="button" class="btn btn-secondary close" data-bs-dismiss="modal">
            取消
          </button>
          <button
            type="button"
            class="btn btn-primary announce-submit"
            @click="handleSubmit"
            data-bs-dismiss="modal"
          >
            送出
          </button>
        </div>
      </div>
    </div>
  </div>
</template>

